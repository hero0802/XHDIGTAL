package com.struct;

public class MarkStruct {
	private int bsId=0;
	private String bsName="";
	private int model=-1;
	private int linkModel=-1;
	private int status=-1;
	public int getBsId() {
		return bsId;
	}
	public void setBsId(int bsId) {
		this.bsId = bsId;
	}
	public String getBsName() {
		return bsName;
	}
	public void setBsName(String bsName) {
		this.bsName = bsName;
	}
	public int getModel() {
		return model;
	}
	public void setModel(int model) {
		this.model = model;
	}
	public int getLinkModel() {
		return linkModel;
	}
	public void setLinkModel(int linkModel) {
		this.linkModel = linkModel;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
	

}
